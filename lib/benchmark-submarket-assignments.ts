export const CURATED_BENCHMARK_ZIP_ASSIGNMENTS_BY_COUNTY_ID = Object.freeze(
  CURATED_BENCHMARK_ZIP_ASSIGNMENTS.reduce<
    Record<string, BenchmarkZipAssignmentSeed[]>
  >((acc, seed) => {
    if (!seed.countyId) return acc
    const current = acc[seed.countyId] ?? []
    current.push(seed)
    acc[seed.countyId] = current
    return acc
  }, {})
)

export function curatedZipAssignmentsForSubmarketId(
  submarketId: string
): readonly BenchmarkZipAssignmentSeed[] {
  return CURATED_BENCHMARK_ZIP_ASSIGNMENTS_BY_SUBMARKET_ID[submarketId] ?? []
}

export function curatedZipAssignmentsForCountyId(
  countyId: string
): readonly BenchmarkZipAssignmentSeed[] {
  return CURATED_BENCHMARK_ZIP_ASSIGNMENTS_BY_COUNTY_ID[countyId] ?? []
}

export function curatedZipAssignmentsForZipCode(
  zipCode: string
): readonly BenchmarkZipAssignmentSeed[] {
  return CURATED_BENCHMARK_ZIP_ASSIGNMENTS_BY_ZIP_CODE[zipCode] ?? []
}
